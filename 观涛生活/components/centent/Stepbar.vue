		methods: {
			updateActiveNav() {
				const navItems = document.querySelectorAll('nav > a');
				const sections = document.querySelectorAll('.content');

				let max = 0;
				let activeSection = null;

				sections.forEach((section, index) => {
					const sectionTop = section.offsetTop - 50; // 减去一些偏移量，确保更早激活  
					if (window.pageYOffset >= sectionTop && (window.pageYOffset < section.offsetTop + section
							.offsetHeight || index === sections.length - 1)) {
						activeSection = navItems[index];
					}
				});

				if (activeSection) {
					navItems.forEach(item => {
						item.style.borderColor = 'transparent';
						item.style.color = '#191919';
					});
					activeSection.style.borderColor = '#6f00ff';
					activeSection.style.color = '#6f00ff';
				}
			},
			observeElement() {
				// 选择你想要观察的元素  
				const elements = [
					this.$refs.myto,
					// this.$refs.mycontent,
					this.$refs.myfaot
				];
				// 第二个元素
				const elmytop = this.$refs.myto;
				// // 第三个元素
				// const elemyfor = this.$refs.myfor;
				// // 第四个
				// const elemycontent = this.$refs.mycontent
				// // 最后一个元素
				// const elemyfaot = this.$refs.myfaot
				// 创建一个IntersectionObserver实例  
				this.observer = new IntersectionObserver((entries) => {
					entries.forEach((entry) => {
						if (entry.isIntersecting) {
							this.mytop = "250"
							// 如果元素进入视口  
							if (this.isScrollingUp) {
								this.mytop = '555';
								this.isScrollingUp = false; // 重置滚动方向标志  
							}
							if (entry.target === elements[elements.length - 1]) {
								// 这是最后一个元素  
								this.mytop = "150";
							}
						}

					});
				}, {
					root: null, // 视口  
					rootMargin: '0px', // 边距  
					threshold: [0, 0.1, 0.5, 1] // 多个阈值  
				});

				// 开始观察元素  
				// this.observer.observe(elemyone);
				// this.observer.observe(elmytop);
				// this.observer.observe(elemyfor);
				// this.observer.observe(elemyfaot);
				// this.observer.observe(elemycontent);
				// 添加滚动事件监听器来跟踪滚动方向  
				elements.forEach(element => {
					if (element) {
						this.observer.observe(element);
					}
				});
				window.addEventListener('scroll', this.handleScroll);
			},
			handleScroll() {
				const currentScrollPosition = window.scrollY || window.pageYOffset || document.documentElement.scrollTop;
				if (currentScrollPosition < this.lastScrollPosition) {
					// 上划  
					this.isScrollingUp = true;
				} else {
					// 下划或未改变（虽然通常不会进入这个else块，但保留以防万一）  
					this.isScrollingUp = false;
				}
				this.lastScrollPosition = currentScrollPosition;
			},
			// 你可以在这里添加其他方法来清理事件监听器和IntersectionObserver，  
			// 例如在组件销毁时调用。  
			beforeDestroy() {
				if (this.observer) {
					this.observer.disconnect();
				}
				window.removeEventListener('scroll', this.handleScroll);
			}

